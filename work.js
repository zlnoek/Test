// PROMPT: Write a for loop than can loop thru any array

var newArray = [1,2,3,4,5];

// for every element double it
for (var i=0; i < newArray.length; i++){
  // i=0, i = index of array
  newArray[i] = newArray[i] * 2;
}

console.log(newArray);

